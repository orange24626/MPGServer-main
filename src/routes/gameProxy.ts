gameProxy.openapi(GameHistoryRoute, async (c) => {


  const history = await GameHistoryService.getGamePlayerHistory(c.req.valid("form"));
  let  formatData = history?.map((item) => {
    const detail = item?.detail as any;
    const bds = detail?.map((dItem: any) => {
      return {
        ...dItem,
        gd: {
          ...dItem.gd,
          mf: {
            mt: dItem.gd?.mf?.mt,
            ms: dItem.gd?.mf?.ms,
            mi: dItem.gd?.mf?.mi,
          },
        },
      };
    });
    return {
      bd: bds,
      bt: moment(moment.utc(item.createdAt).format("YYYY-MM-DDTHH:mm:ss")).toDate().getTime(),
      tid: item.historyId.toString(),
      ge: item.ge,
      fscc: item.fscc,
      cc: item.currency,
      gid: item.gameID,
      gtba: +(+item.totalBet).toFixed(2),
      gtwla: +(+item.profit).toFixed(2),
      mgcc: +(+item.mgcc).toFixed(2),
    };
  });

  return c.json({
    dt: {
      bh: formatData,
    },
    err: null,
  });
});

gameProxy.openapi(GameHistoryStaticRoute, async (c) => {
  const formData = c.req.valid("form");
  const { result, lastRecord } = await GameHistoryService.getGamePlayerHistoryStatic(formData);
  const { count, tb, profit } = result;
  return c.json({
    dt: {
      bs: {
        gid: Number(formData.gid),
        bc: count ? +new Decimal(count.toString()).toFixed(2) : 0,
        btba: tb ? +new Decimal(tb).toFixed(2) : 0,
        btwla: profit ? +new Decimal(profit).toFixed(2) : 0,
        lbid: lastRecord?.historyId.toString(),
      },
      lut: lastRecord?.createdAt.getTime(),
    },
    err: null,
  });
});

