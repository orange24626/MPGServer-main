gameProxy.post("/GameName/Get", async (c) => {
  const formData = await c.req.formData();
  const atk = formData.get("atk");
  if (!atk) {
    throw new HTTPException(401, { message: "atk is required" });
  }
  const session = await AuthService.verifyGameToken(atk as string);
  if (!session) {
    throw new HTTPException(401, { message: "Invalid token" });
  }
  const { rows: games } = await PgClient.query<Game>(`
    SELECT "gameID", "fullName"
    FROM "public"."Game"
  `);
  const gameObj: any = {};
  games.forEach((game) => {
    gameObj[game.gameID] = game.fullName;
  });
  return c.json({
    dt: gameObj,
    err: null,
  });
});

